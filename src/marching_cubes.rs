static EDGE_TO_VERTS: &[(usize, usize); 12] = &[
    (0, 1), //0
    (1, 2), //1
    (2, 3), //2
    (3, 0), //3
    (4, 5), //4
    (5, 6), //5
    (6, 7), //6
    (7, 4), //7
    (4, 0), //8
    (5, 1), //9
    (6, 2), //10
    (7, 3), //11
];

#[derive(Copy, Clone, Default)]
pub struct Vertex {
    pub position: Vec3,
    pub distance: f32,
    pub normal: Vec3,
    pub index: usize, // flat index in grid
}

impl Vertex {
    pub fn new(position: Vec3, distance: f32, normal: Vec3, index: usize) -> Self {
        Self {
            position,
            distance,
            normal,
            index,
        }
    }
}

pub struct MarchingCubes {}

impl MarchingCubes {
    fn vertex_lerp_t(a: &Vertex, b: &Vertex) -> f32 {
        if (a.distance - b.distance).abs() < 0.00001 {
            0.0
        } else {
            let isolevel = 0.0;
            ((isolevel - a.distance) / (b.distance - a.distance)).clamp(0.0, 1.0)
        }
    }

    fn get_cube_index(distances: &[f32; 8]) -> Option<usize> {
        let mut cubeindex: usize = 0;
        for (i, &distance) in distances.iter().enumerate() {
            cubeindex |= usize::from(distance < 0.0) << i;
        }

        if EDGE_TABLE[cubeindex] != 0 {
            Some(cubeindex)
        } else {
            None
        }
    }

    fn march_cube(
        grid: &[Vertex; 8],
        cubeindex: usize,
        mesh: &mut TriangleMesh,
        vidx_lookup: &mut HashMap<(usize, usize), u32>,
    ) -> u32 {
        let mut vertlist = [0_u32; 12];
        for edge in 0..12 {
            if (EDGE_TABLE[cubeindex] & (1 << edge)) != 0 {
                let verts = EDGE_TO_VERTS[edge];

                let a = &grid[verts.0];
                let b = &grid[verts.1];

                let edge_id = (a.index.min(b.index), a.index.max(b.index));
                let vidx = *vidx_lookup.entry(edge_id).or_insert_with(|| {
                    let vidx = mesh.positions.len() as u32;
                    let t = Self::vertex_lerp_t(a, b);
                    mesh.positions.push(a.position.lerp(b.position, t).into());
                    mesh.normals
                        .push(a.normal.lerp(b.normal, t).normalize().into());
                    vidx
                });

                vertlist[edge] = vidx;
            }
        }

        // Create the triangles
        let mut ntriang = 0;
        let mut i = 0;

        while TRI_TABLE[cubeindex][i] != -1 {
            for _ in 0..3 {
                let vidx = vertlist[TRI_TABLE[cubeindex][i] as usize];
                mesh.indices.push(vidx);
                i += 1;
            }
            // Get winding order correct:
            let n = mesh.indices.len();
            mesh.indices.swap(n - 2, n - 1);

            ntriang += 1;
        }

        ntriang
    }
}

impl<T> Grid3<T>
where
    T: SignedDistance,
{
    /// Gives a mesh using colors from the given function
    pub fn marching_cubes_with_color(
        &self,
        color: impl Fn(Vec3) -> Vec3 + Send + Sync,
    ) -> TriangleMesh {
        let mut mesh = self.marching_cubes();
        #[cfg(feature = "with_rayon")]
        {
            use rayon::prelude::*;
            mesh.colors = mesh
                .positions
                .par_iter()
                .map(|p| color(Vec3::new(p[0], p[1], p[2])).into())
                .collect();
        }
        #[cfg(not(feature = "with_rayon"))]
        {
            mesh.colors = mesh
                .positions
                .iter()
                .map(|p| color(Vec3::new(p[0], p[1], p[2])).into())
                .collect();
        }
        mesh
    }

    /// Does NOT return colors
    pub fn marching_cubes(&self) -> TriangleMesh {
        let size = self.size();
        if size[0] <= 1 || size[1] <= 1 || size[2] <= 1 {
            return Default::default();
        }

        let max_vertices = (size[0] * size[1] * size[2]).clamp(1, 65536);

        let mut mesh = TriangleMesh {
            indices: Vec::with_capacity(max_vertices),
            positions: Vec::with_capacity(max_vertices),
            normals: Vec::with_capacity(max_vertices),
            colors: Default::default(), // no colors
        };

        let mut vidx_lookup = HashMap::with_capacity(max_vertices);

        let data = self.data();

        // Setup strides
        let xs = 1;
        let ys = size[0];
        let zs = size[0] * size[1];

        for z in 0..(size[2] - 1) {
            for y in 0..(size[1] - 1) {
                let mut index = y * ys + z * zs;

                for x in 0..(size[0] - 1) {
                    let offsets = [
                        index,
                        index + xs,
                        index + xs + ys,
                        index + ys,
                        index + zs,
                        index + xs + zs,
                        index + xs + ys + zs,
                        index + ys + zs,
                    ];

                    let distances = [
                        data[offsets[0]].distance(),
                        data[offsets[1]].distance(),
                        data[offsets[2]].distance(),
                        data[offsets[3]].distance(),
                        data[offsets[4]].distance(),
                        data[offsets[5]].distance(),
                        data[offsets[6]].distance(),
                        data[offsets[7]].distance(),
                    ];

                    let cubeindex = MarchingCubes::get_cube_index(&distances);

                    if let Some(cubeindex) = cubeindex {
                        let get_vertex =
                            |x: usize, y: usize, z: usize, index: usize, distance: f32| -> Vertex {
                                let pos = Vec3::new(x as f32, y as f32, z as f32);
                                let normal = self.fast_gradient(x, y, z, index, ys, zs);
                                Vertex::new(pos, distance, normal, index)
                            };

                        let grid_vertices = [
                            get_vertex(x + 0, y + 0, z + 0, offsets[0], distances[0]),
                            get_vertex(x + 1, y + 0, z + 0, offsets[1], distances[1]),
                            get_vertex(x + 1, y + 1, z + 0, offsets[2], distances[2]),
                            get_vertex(x + 0, y + 1, z + 0, offsets[3], distances[3]),
                            get_vertex(x + 0, y + 0, z + 1, offsets[4], distances[4]),
                            get_vertex(x + 1, y + 0, z + 1, offsets[5], distances[5]),
                            get_vertex(x + 1, y + 1, z + 1, offsets[6], distances[6]),
                            get_vertex(x + 0, y + 1, z + 1, offsets[7], distances[7]),
                        ];

                        let _num_tris = MarchingCubes::march_cube(
                            &grid_vertices,
                            cubeindex,
                            &mut mesh,
                            &mut vidx_lookup,
                        );
                    }

                    index += 1;
                }
            }
        }

        mesh
    }
}
